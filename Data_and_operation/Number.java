
public class Number {
	public static void main(String[] args) {
		
		// Operator(연산자)
		System.out.println(6+2);
		System.out.println(6-2);
		System.out.println(6*2);
		System.out.println(6/2);
		System.out.println(6%2);
		
		System.out.println(Math.PI);
		System.out.println(Math.floor(Math.PI)); // floor : 소숫점을 없앰(내림)
		System.out.println(Math.ceil(Math.PI)); // ceil : 소숫점을 없앰(올림)
	}
}
